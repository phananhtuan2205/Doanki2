    private void tbl_DataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_DataMouseClicked
        //ẩn nút new , hiện nút update và nút delete: (hàm setEnable )
        btn_New.setEnabled(false);
        btn_Update.setEnabled(true);
        btn_Delete.setEnabled(true);

        //lấy ra dòng cần update -> tbl_Data.getSelectedRow()
        int row = tbl_Data.getSelectedRow();

        // đổ data ở dòng vừa chọn ( hàm .getValueAt ) lên các ô input
        text_MaKhoa.setText(tbl_Data.getValueAt(row, 0).toString());
        text_TenKhoa.setText(tbl_Data.getValueAt(row, 1).toString());
        text_SDT.setText(tbl_Data.getValueAt(row, 2).toString());

        text_MaKhoaCu.setText(tbl_Data.getValueAt(row, 0).toString());
    }//GEN-LAST:event_tbl_DataMouseClicked

    private void ResetForm() {
        text_MaKhoa.setText("");
        text_SDT.setText("");
        text_TenKhoa.setText("");

        //focus vao o ma khoa
        text_MaKhoa.requestFocus();
    }

    private void loaddata() {
        //truoc tien can xoa table cu (nhung dong co rowcount > 0)
        while (tbl.getRowCount() > 0) {
            tbl.removeRow(0);
        }

        //lay du lieu fill vao tbl
        ArrayList<Khoa> khoas = new KhoaDAO().search("");

        for (Khoa khoa : khoas) {

            tbl.addRow(new Object[]{
                khoa.getMakhoa(),
                khoa.getTenkhoa(),
                khoa.getSdt(),
                khoa.getCreated_at(),
                khoa.getUpdated_at()
            });
        }

        tbl_Data.setModel(tbl);
    }

    private boolean checkInfo() {
        Validate v = new Validate();

        //neu check ma khoa khong hop le -> 
        if (!v.checkID(text_MaKhoa.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập Mã khoa sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            text_MaKhoa.setText("");
            text_MaKhoa.requestFocus();
            return false;

            //neu ten khoa khong hop le
        } else if (!v.checkExistSpace(text_TenKhoa.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập tên Khoa sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            text_TenKhoa.setText("");
            text_TenKhoa.requestFocus();
            return false;

            //neu sdt khong hop le
        } else if (!v.checkSpace(text_SDT.getText()) || !v.checkPhone(text_SDT.getText())) {
            JOptionPane.showMessageDialog(this, "Lỗi Số Điện Thoại", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            text_SDT.setText("");
            text_SDT.requestFocus();
            return false;
        }
        return true;
    }
    

