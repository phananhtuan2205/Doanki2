    private void btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UpdateActionPerformed
        //chỉ hiển thị khi đang ở chế độ update duex liệu ( nút addnew bị ẩn )
        
        //check exist 
        if(new KhoaDAO().checkExists(text_MaKhoa.getText()) && !text_MaKhoa.getText().equalsIgnoreCase(text_MaKhoaCu.getText()) ){
            JOptionPane.showMessageDialog(this, "Mã khoa đã tồn tại , vui lòng chọn một mã khoa khác !");
            return;
        }
        Khoa k = new Khoa();
        k.setMakhoa(text_MaKhoa.getText());
        k.setTenkhoa(text_TenKhoa.getText());
        k.setSdt(text_SDT.getText());

        new KhoaDAO().updateByID(text_MaKhoaCu.getText(), k);

        text_MaKhoaCu.setText("");
        btn_Reset.doClick();
        loaddata();
    }//GEN-LAST:event_btn_UpdateActionPerformed

    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed

        String maKH_for_del = text_MaKhoaCu.getText();
        int chon = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa khoa này (mã khoa " + maKH_for_del + " ) ?");

        //        System.out.println(chon); -> yes = 0 ; no = 1 , cancell = 2
        if (chon == 0) {
            new KhoaDAO().deleteKhoa(maKH_for_del);
            loaddata();
            btn_Reset.doClick();
        }
    }//GEN-LAST:event_btn_DeleteActionPerformed

    private void btn_NewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_NewActionPerformed
        // khi nhan nut save:

        // validate cac field
        if (!checkInfo()) {
            return;
        }
        if(new KhoaDAO().checkExists(text_MaKhoa.getText())){
            JOptionPane.showMessageDialog(this, "Mã khoa đã tồn tại , vui lòng chọn một mã khoa khác !");
            return;
        }
        //neu check ok -> khoi tao mot obj khoa -> insert vaof table thong qua DAO
        Khoa k = new Khoa();
        k.setMakhoa(text_MaKhoa.getText());
        k.setTenkhoa(text_TenKhoa.getText());
        k.setSdt(text_SDT.getText());
        k.setCreated_at(utils.DateTime.getTimeNow("yyyy-MM-dd hh:mm:ss"));
        k.setUpdated_at(utils.DateTime.getTimeNow("yyyy-MM-dd hh:mm:ss"));

        new KhoaDAO().addNew(k);

        //add xong thi reset form va load lai data
        ResetForm();
        loaddata();
    }//GEN-LAST:event_btn_NewActionPerformed

