    private void tbl_DiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_DiemMouseClicked
        // TODO add your handling code here:
        btn_chonMH.setEnabled(false);
        i = tbl_Diem.getSelectedRow();

        Diem diem = dataList.get(i);
        choosenMH = new MonHocDAO().searchID(diem.getMamh()).get(0);
        text_maMH.setText(choosenMH.getMaMH());

        choosenGV = new GVDAO().getByID( diem.getMaGV()).get(0);
        text_GV.setText(choosenGV.getTenGV());
        
        //lấy ra choosen SV từ bảng:
        String maSV = diem.getMaSV();
        choosenSV = new SinhvienDAO().getByID(maSV).get(0);
        text_maSV.setText(choosenSV.getTenSV());

        text_diem.setText(Float.toString(diem.getDiem()));

        btn_update.setEnabled(true);
        btn_delete.setEnabled(true);
        
        text_diem.setEnabled(true);
    }//GEN-LAST:event_tbl_DiemMouseClicked

    public void receiveLop(Lop lop){
        choosenLop = lop;
        text_maLop.setText(choosenLop.getMaLop());
        searchLop.setVisible(false);
        
        if (choosenMH!=null) {
            loadDdataa();
        }
    }
