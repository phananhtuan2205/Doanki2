    private boolean checkInfo() {
        Validate v = new Validate();
        
        if (choosenGV==null) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn giảng viên", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            btn_chonGV.requestFocus();
            return false;
        }
        else if (!v.checkDiem(text_diem.getText())) {
            JOptionPane.showMessageDialog(this, "Điểm không hợp lệ", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            text_diem.requestFocus();
            return false;
        }
        return true;
    }

    private void loadDdataa() {
        tab.setTitleAt(0, "Danh sách điểm môn "+choosenMH.getMaMH()+" - Lớp "+choosenLop.getMaLop());
        //xoa dât cu
        while(tbl_data.getRowCount()>0){
            tbl_data.removeRow(0);
        }
       
        dataList = new DiemDAO().getByLop_MonHoc(choosenLop.getMaLop(), choosenMH.getMaMH());
        
        for (Diem diem : dataList) {
            
            tbl_data.addRow(new Object[]{
                tbl_data.getRowCount() + 1,
                diem.getMaSV(),
                new SinhVien.SinhvienDAO().getByID(diem.getMaSV()).get(0).getTenSV(),
                diem.getMamh(),
                diem.getDiem(),
                new GVDAO().getByID(diem.getMaGV()).get(0).getTenGV() ,
                diem.getCreated_at(),
                diem.getUpdated_at()
            });
        }
        tbl_Diem.setModel(tbl_data);
    }
